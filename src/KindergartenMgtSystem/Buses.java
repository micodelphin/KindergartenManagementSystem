    private void addBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBtnActionPerformed
       
        if (busPlateTxt.getText().isEmpty() || driverTxt.getText().isEmpty() || driverNmbrTxt.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Incomplete information");
        } else if (!busPlateTxt.getText().matches("[A-Z]{3}\\s+\\d{3}\\s+[A-Z]{1}")) {
            JOptionPane.showMessageDialog(this, "Bus plate should be in the format 'RAD 705 R'");
        } else if (driverNmbrTxt.getText().length() != 10) {
            JOptionPane.showMessageDialog(this, "Phone number should be 10 characters");
        }else{
            try {
                con = DriverManager.getConnection(dbUrl, username, password);
                
                // Check if Passenger_id already exists
                String checkBusPlateQuery = "SELECT * FROM buses WHERE bus_plate=?";
                PreparedStatement checkBusPlateStatement = con.prepareStatement(checkBusPlateQuery);
                checkBusPlateStatement.setString(1, busPlateTxt.getText());
                ResultSet existingbusPlate = checkBusPlateStatement.executeQuery();

                if (existingbusPlate.next()) {
                    JOptionPane.showMessageDialog(this, "The bus already exist.");
                    return; // Stop further processing
                }

                // If Passenger_id does not exist, proceed with the insertion
                
                String sql = "insert into buses(bus_plate,driver_name,driver_number) values(?,?,?)";
                PreparedStatement Add = con.prepareCall(sql);
//                Add.setInt(1, busId);
                Add.setString(1, busPlateTxt.getText());
                Add.setString(2, driverTxt.getText());
                Add.setString(3, driverNmbrTxt.getText());
                int rowsAffected = Add.executeUpdate();
                con.close();
                DisplayBuses();
                Clear();
                if (rowsAffected >= 1) {
                    JOptionPane.showMessageDialog(this, "bus Saved Successfully !!!");
                } else {
                    JOptionPane.showMessageDialog(this, "bus Not Saved !!!");
                }
                
            } catch (Exception e) {
                e.printStackTrace();
                JOptionPane.showMessageDialog(this, "Server Error !!!");
            }
        }

    }//GEN-LAST:event_addBtnActionPerformed

