    private void editBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editBtnMouseClicked
       
      if (Key == 0) {
        JOptionPane.showMessageDialog(this, "Select a bus to update");
    } else {
        try {
            con = DriverManager.getConnection(dbUrl, username, password);
            String sql = "UPDATE buses SET bus_plate=?, driver_name=?, driver_number=? WHERE bus_id=?";
            PreparedStatement update = con.prepareStatement(sql);
            update.setString(1, busPlateTxt.getText());
            update.setString(2, driverTxt.getText());
            update.setString(3, driverNmbrTxt.getText());
            update.setInt(4, Key); // Assuming Key is the bus_id
            int rowsAffected = update.executeUpdate();
            con.close();
            DisplayBuses();
            Clear();
            if (rowsAffected >= 1) {
                JOptionPane.showMessageDialog(this, "Bus updated successfully");
            } else {
                JOptionPane.showMessageDialog(this, "Bus not updated");
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Server error");
        }
    }
    }//GEN-LAST:event_editBtnMouseClicked

